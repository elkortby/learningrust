pub fn run() {
	// Print to console
	println!("Hello World!");

	// Basic Formating
	println!("{} is from {} and each time we make curly braces we pass an arg", 23, "test");

	// Positional Args
	println!("{0} is a {2} and {1} is a {3}", "oussama", "safia", "men", "woman");

	println!("{name} is a developer", name = "oussama");	
}